#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t					index;
	const unsigned char		*str1;
	const unsigned char		*str2;

	index = 0;
	str1 = s1;
	str2 = s2;
	while (index < n)
	{
		if (str1[index] != str2[index])
			return (str1[index] - str2[index]);
		index++;
	}
	return (0);
}
